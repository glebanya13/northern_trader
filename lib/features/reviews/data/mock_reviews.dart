  /// Получить обзоры по категории
  static List<Review> getReviewsByCategory(String category) {
    return getMockReviews()
        .where((review) => review.category == category)
        .toList();
  }

  /// Получить последние N обзоров
  static List<Review> getLatestReviews({int limit = 3}) {
    final reviews = getMockReviews();
    reviews.sort((a, b) => b.createdAt.compareTo(a.createdAt));
    return reviews.take(limit).toList();
  }
